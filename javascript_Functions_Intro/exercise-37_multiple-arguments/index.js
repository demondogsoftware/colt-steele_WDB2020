// write a function called isSnakeEyes which accepts two inputs representing dice
// if both numbers are 1 print Snake Eyes! otherwise print Not Snake Eyes!

function isSnakeEyes(die1, die2) {
  if (die1 === 1 && die2 === 1) {
    console.log('Snake Eyes!');
  } else {
    console.log('Not Snake Eyes!');
  }
}
